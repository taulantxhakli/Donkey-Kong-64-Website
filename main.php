    <h2>Bosses</h2>
    <h4>King Krusha K. Rool</h4>
    <img src="assets/king.jpg" style="height:500px;">
    <p>The King of the Kremlings. Donkey Kong's mortal enemy. This fight will test the player like never before. All Kongs will have a turn in battering the king of Kremlings
      in order to stop him. Be warned, King K. Rool does not play fair, as his Kremling minions save him from the bell every knock-down.
    </p>
    <img src="assets/donkeykong.gif" style="height:300px;"><img src="assets/kingkkrool.gif" style="height:300px;">
    <p>Donkey Kong, the first Kong to challenge K. Rool, needs to keep his distance from the Kremling leader. As K. Rool 
    will slam down on the canvas and deliver a devastating blow (as shown on the left). Once K. Rool is in the middle of the ring, barrels will come
    down on top of the ring edges. Which is where DK can get into the barrel and shoot himself to the giant croc (as shown on the right).
    </p>
    <img src="assets/firstphasedk.gif" style="height:300px;"><img src="assets/secondphasedk.gif" style="height:300px;">
    <p>As the player progresses, they will need to use the whole Kong family to defeat K. Rool. 
    Question is, will you be able to stop K. Rool's fury?
    </p>
    <br>
    <h4>Army Dillo</h4>
    <img src="assets/armadillo.jpg" style="height:500px;">
    <p>Army Dillo is the first boss that Donkey Kong faces against in the Jungle Japes level. He has two stages, one is him with the two cannons, with the other not having cannons. DK needs to use the explosive barrel in order to defeat him.
      In the second fight, the Kremlings will upgrade the boss to try and defeat DK. This time, the Army Dillo will have jetpack launchers (as shown on the left) where his rocket launchers are to hover over the ground and slam down to deliver a devastating blow.
      Once the armadillo is finished, he will replace the jetpack with the rocket launchers after a roll back to the center (as shown on the right) to fire at DK once again.
    </p>
    <img src="assets/armydillorockets.gif" style="height:300px;"><img src="assets/rocket.gif" style="height:300px;">
    <br>
