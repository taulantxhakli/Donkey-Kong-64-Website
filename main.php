    <img src="assets/fly.jpg" style="height:500px;">
    <p>Mother to the baby Dogadon, which was squashed by Diddy Kong, she is the second boss encounter in the Angry Aztec level and is challenged by Diddy himself. Though, this will not be the only time the 
      player will face this beast. In the Fungi Forest level, Chunky Kong finds himself stuck in the same lava pit to face off against Dogadon once more.
    </p>
