    <h2>Bosses</h2>
    <h4>King Krusha K. Rool</h4>
    <img src="assets/king.jpg" style="height:500px;">
    <p>King Krusha K. Rool is the boxer alias of King K. Rool. He is the final boss of Donkey Kong 64. 
    K. Rool will fight the Kongs one by one, each with a different strategy to be countered; normal attacks will be ineffective, and except at certain points with certain Kongs, all special attacks are disabled, including weapons and instruments. 
    In each round, the Kong fighting has a time limit of three minutes to defeat K. Rool. 
    If time runs out, the current Kong must start the round over; K. Rool's damage will be reset, but whatever damage the Kong received will remain. 
    Each new Kong begins with maximum health. 
    If a Kong's health runs out, or if the Kongs fail to defeat K. Rool within twelve rounds, they must start the entire fight over.
    </p>
    <img src="assets/donkeykong.gif" style="height:300px;"><img src="assets/kingkkrool.gif" style="height:300px;">
    <br>
    <h4>Army Dillo</h4>
    <img src="assets/armadillo.jpg" style="height:500px;">
    <p>Army Dillo is the first boss that Donkey Kong faces against in the Jungle Japes level. He has two stages, one is him with the two cannons, with the other not having cannons. DK needs to use the explosive barrel in order to defeat him.
      In the second fight, the Kremlings will upgrade the boss to try and defeat DK. This time, the Army Dillo will have jetpack launchers (as shown on the left) where his rocket launchers are to hover over the ground and slam down to deliver a devastating blow.
      Once the armadillo is finished, he will replace the jetpack with the rocket launchers after a roll back to the center (as shown on the right) to fire at DK once again.
    </p>
    <img src="assets/armydillorockets.gif" style="height:300px;"><img src="assets/rocket.gif" style="height:300px;">
    <br>
    <h4>Dogadon</h4>
    <img src="assets/fly.jpg" style="height:500px;">
    <p>Mother to the baby Dogadon, which was squashed by Diddy Kong (as shown on the left), she is the second and fifth boss in Donkey Kong 64. Diddy Kong fights him in Angry Aztec, the second level of the game, and Chunky Kong fights him in Fungi Forest, the fifth level of the game.
    Dogadon will hover at the corners of the arena and spit fireballs, always aiming at Diddy. After a set number of shots, he will land and then issue an unintelligible verbal insult. This is Diddy's chance; by grabbing the TNT barrel from the pad in the center of the arena and throwing it at Dogadon, he will damage the dragon and cause him to fall into the lava. Immediately afterwards, he shoots back up into the air, spins around to put out the flames on his body, and resumes his onslaught (as shown on the right).
    </p>
    <img src="assets/dogadonkidsmash.gif" style="height:300px;"><img src="assets/dogadonbarrel.gif" style="height:300px;">
    <br>
    <h4>Mad Jack</h4>
    <img src="assets/madjack.jpg" style="height:500px;">
    <p>Mad Jack is the third boss in Donkey Kong 64. Tiny Kong fights him in Frantic Factory, the third world of the game.
    When Tiny enters Mad Jack's arena, she'll find a large square platform with sixteen blue and white squares spread evenly apart, lighting up in various alternating patterns. 
    When Tiny moves onto the platform, all of the squares except for one white one will stop glowing. When she steps onto this square, all sixteen of them elevate by a few stories. 
    Then, from a square slot in the ceiling, a colorful box drops down and leaps closer to Tiny. When it reaches a blue square adjacent to her, the box grows larger. 
    A few seconds later, Mad Jack emerges, and the battle begins (as shown on the left). Mad Jack will jump around a set number of times before emerging from his box to attack. 
    As he jumps from square to square, Tiny must dodge his jumping with the Ponytail Twirl move she learned from Cranky Kong. 
    Once Mad Jack appears from his box, Tiny will need to find the platform that is the same color as the one he is on and press the switch located on the platform. 
    Slamming the correct switch will make the platform Mad Jack is on release an electric shockwave, injuring him (as shown on the right). However, slamming on the switch on the platform of the incorrect color will electrocute Tiny Kong instead, costing her a slice of health.
    Tiny must electrocute Mad Jack five times to defeat him.
    </p>
    <img src="assets/madjackintro.gif" style="height:300px;"><img src="assets/madjackattack.gif" style="height:300px;">
    <br>
    <h4>Puftoss</h4>
    <img src="assets/puftoss.jpg" style="height:500px;">
    <p>Puftoss is the fourth boss in Donkey Kong 64. Lanky Kong fights him in Gloomy Galleon, the fourth world of the game (as shown on the left). Lanky Kong must navigate the boat around the arena, avoiding Puftoss' fireballs, shockwaves, and smaller Puftups as he drives the boat through the DK Stars on the water's surface. 
    After driving through a star, an electric pole will rise out of the water and a 30-second countdown will start. 
    Lanky must reach the next star before the countdown reaches 0; once all five poles have surfaced, they will produce an electric current that shocks Puftoss. 
    After each shock, the stars get smaller and the countdown decreases by five seconds; if time runs out, all of the poles will retract into the water, and Lanky must start again (as shown on the right).
    Lanky must electrocute Puftoss five times to defeat him.</p>
    <img src="assets/puftossintro.gif" style="height:300px;"><img src="assets/puftossbattle.gif" style="height:300px;">
    <br>
    <h4>King Kut Out</h4>
    <img src="assets/kkutout.jpg" style="height:500px;">
    <p>As the Kongs go through the Creepy Castle level, they are met with the boss himself...King Kut Out? The Kremlings create a cut out of King K. Rool to scare the Kongs, but they do not know that the Kongs see right through the cardboard.
      To defeat this art project, the Kongs need to wait for the Kut Out to appear for them to launch themselves through the cannons and break off the pieces of cardboard from Kut Out (as shown on the left).
      But, the Kongs need to tred carefully, as the Kut Out will shoot lasers out of its eyes while Kremlings dressed as ghosts will distract the Kongs (as shown on the right).
    </p>
