    <h2>All Levels in DK 64</h2>
    <h4>DK Treehouse</h4>
    <img src="assets/dktreehouseintro.gif" style="height:400px;">
    <p>The intro to the game sees Donkey Kong in his famous treehouse exercising before he sets out to fight the Kremlings...and retrieve his golden bananas.</p>
    <br>
    <h4>DK Isles Overview</h4>
    <img src="assets/dkislesoverview.png" style="height:500px;">
    <p>Once the player goes through the trails to master the controls of the gorilla, they will be greeted to the outside of the island, the Isles itself.
      The first place that is needed to go is to K. Lumsy's prison. The Kong family agree to free K. Rool's little brother from the iron bars, in turn, K. Lumsy will
      open the level entrances, first being Jungle Japes.
    </p>
    <br>
    <h4>Jungle Japes</h4>
    <img src="assets/Jungle_Japes_center.png" style="height:500px;">
    <p>Jungle Japes is the first level in Donkey Kong 64. Since this is the first adventure stage of the game, it is where the player learns to do many things, including collecting bananas and transforming into Rambi the Rhino. It is located in Donkey Kong Island and requires one Golden Banana (found near the entrance itself) to enter. Diddy Kong is held captive here in a mountain prison.
      Jungle Japes, as the name suggests, has many jungle areas, filled with climbable trees and collectibles. There are many cliffs that the Kongs climb, one of which has a mine area. The jungle also has many gloomy tunnels full of swinging light bulbs and Gnawties. The Kasplats are found in the tunnels, except the one carrying Chunky Kong's Blueprint is located in the underground cave. The boss of this stage is Army Dillo, which requires 60 regular Bananas both from Donkey Kong and Diddy Kong to enter, and Army Dillo is fought by Donkey Kong.
    </p>
    <br>
    <h4>Angry Aztec</h4>
    <img src="assets/angryaztec.png" style="height:500px;">
    <p>Angry Aztec is the second level of Donkey Kong 64. 
      It is located in the desert, and it models the ancient Aztec civilization, including some Egyptian inspiration. 
      Kongs must have at least five Golden Bananas for B. Locker to allow them into the desert world. 
      Most of the area is engulfed in quicksand, which damages the Kongs until they are out of melons. 
      Lanky Kong and Tiny Kong are the other Kongs that are held captive in separate temples and become playable once freed by Donkey Kong and Diddy Kong. 
      It is the first level to have Candy's Music Shop, and by extension, Music Pads and the use of instruments.</p>
    <br>
    <h4>Frantic Factory</h4>
    <img src="assets/Frantic_Factory.png" style="height:500px;">
    <p>Frantic Factory is the third level of Donkey Kong 64. It is a rogue toy manufacturing factory in which Chunky Kong is held captive. The Kongs can access the level's lobby after unlocking the second padlock of K. Lumsy's cage. The level requires 15 Golden Bananas to enter, and the boss is Mad Jack. There is one room with a playable Donkey Kong arcade cabinet, and by completing it, the Kongs win the Nintendo Coin.
    </p>
    <br>
    <h4>Gloomy Galleon</h4>
    <img src="assets/Gloomygalleon.png" style="height:500px;">
    <p>Gloomy Galleon is the fourth level in Donkey Kong 64. It is a water-based cove area featuring King K. Rool's sunken galleon from earlier Donkey Kong franchise games. The stage has a mild pirate theme with both underwater and cave-like areas. Players have to use Lanky Kong to defeat the area's boss, which is Puftoss. In this level, Lanky Kong can also transform into Enguarde the Swordfish.
    </p>
    <br>
    <h4>Fungi Forest</h4>
    <img src="assets/Fungiforest.jpg" style="height:500px;">
    <p>Fungi Forest, as the name states, features numerous forests of mushrooms. The stage centers around a giant Cuckoo Clock, which has the ability to change day into night and vice-versa. Most of the areas of this world are only accessible during certain times of the day, and enemies are also affected by time as well, often becoming more hostile when day becomes night.
    </p>
    <br>
    <h4>Crystal Caves</h4>
    <img src="assets/Crystal_Caves.png" style="height:500px;">
    <p>Crystal Caves is the sixth level in Donkey Kong 64 (however, the player can choose to do Creepy Castle before entering this one). As the name suggests, it is a cave made of ice. The caves inside the highlands of the isles are encrusted with rimes of ice, as well as jagged ore deposits. A giant Kosha lurks in the highest part of the cave, raining stalactites down on the unsuspecting Kongs. The area has all the Kong shops in it. The area's boss is a rematch with Army Dillo, the boss from Jungle Japes, and requires 350 regular Bananas to give to Scoff and allow Troff to unlock the door to the boss.
    </p>
    <br>
    <h4>Creepy Castle</h4>
    <img src="assets/Creepy_Castle.png" style="height:500px;">
    <p>The Kongs can only enter this level from its lobby from using a large Barrel Cannon near K. Lumsy's Prison. Although Creepy Castle is said to be located on K. Rool's Crocodile Isle, it also appears to be floating above ground. If the active Kong falls off the location, they respawn back from the beginning portal. The level takes place both inside and outside of the old, abandoned castle.

        The outside involves a high climb around the tall castle. Like typical haunted areas, a continuous thunderstorm takes place in the area. Many of the enemies featured are undead, such as Bones and Ghosts, and is inhabited by Bats. The castle interior features haunted corridors, mostly-empty rooms, and haunted catacombs on the lowest floor.

      Additionally, this level is the first to have the red Kong Switches, which can only be activated from using the Super Duper Simian Slam move learned from Cranky Kong. In this level, the red switches are usually used for opening entrances to various parts of the castle.
    </p>
    <br>
    <h4>Hideout Helm</h4>
    <img src="assets/Hideout_Helm_start.png" style="height:500px;">
    <p>Hideout Helm is the eighth and final level of Donkey Kong 64. It is the Kremling Krew's battle station located inside the head of Crocodile Isle. The lobby is accessible after the Kongs unlock Padlocks 6 and 7 on K. Lumsy's cage (meaning the player must collect the Boss Keys in Crystal Caves and Creepy Castle). To enter the level itself, the Kongs must have a minimum of 100 Golden Bananas.
      This is the only level in Donkey Kong 64 in which a Kasplat does not appear (excluding its appearance in the Battle Arena as well as the Kasplat that K. Rool sicced one of his giant Klaptraps on in a prior cutscene).
    </p>
